/**
 * 시/도 목록만 추출
 */
export const CITY_NAMES = LOCATIONS.map((city) => city.name);

/**
 * 특정 시의 구/군 목록 추출
 */
export function getDistricts(cityName: string): string[] {
  const city = LOCATIONS.find((c) => c.name === cityName);
  return city ? city.districts.map((d) => d.name) : [];
}

/**
 * 특정 구/군의 동 목록 추출
 */
export function getDongs(cityName: string, districtName: string): string[] {
  const city = LOCATIONS.find((c) => c.name === cityName);
  if (!city) return [];

  const district = city.districts.find((d) => d.name === districtName);
  return district ? district.dongs : [];
}

/**
 * 주소 포맷팅 (시 구 동 → "서울특별시 강남구 역삼동")
 */
export function formatAddress(city: string, district: string, dong: string): string {
  return `${city} ${district} ${dong}`;
}

/**
 * 주소 파싱 ("서울특별시 강남구 역삼동" → {city, district, dong})
 */
export function parseAddress(address: string): {
  city: string;
  district: string;
  dong: string;
} | null {
  const parts = address.split(' ').filter(Boolean);
  if (parts.length < 2) return null;

  return {
    city: parts[0],
    district: parts[1],
    dong: parts[2] || '',
  };
}
